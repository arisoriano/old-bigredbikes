        <h2>Where can I get a bike?</h2>
	<p>Over 30 bikes are available at Appel Commons, Mann Library, and Uris Library.</p>
	
        <h2>When can I get a bike?</h2>
	<p>Bikes are borrowed from a station&#39;s circulation or service desk, therefore our hours are the same as theirs. Station hours change constantly,
        so ask the staff about desk hours if this may be an issue for you. Hours for Mann and Uris Library are posted <a href="http://www.library.cornell.edu/hours" class='page_links' target='_blank'>here</a>. Hours for Appel Commons are posted <a href="http://living.sas.cornell.edu/live/community/studentcenters/communitycenters/appel.cfm" class='page_links' target='_blank'>here</a>.</p>
	
        <h2>How long can I keep the bike?</h2>
	<p>Bikes must be returned a half hour before closing. You may not check out bikes overnight. If you are late, you may be subjected to a late fee
        billed to your bursar bill.</p>
	
        <h2>What if I keep the bike overnight?</h2>
	<p>If you do not return the bike by the end of the day, you may be subject to a late fee of $10 for the first night up to 11am and $10/hour
        thereafter, up to the full cost of replacing the bike and all accessories ($700). Those returning a late bike cannot renew or check out a bike for
        the rest of the day.</p>
	
        <h2>How should I lock my bike?</h2>
	<p>Try to lock both the frame and the front wheel to a bike rack. Users are responsible for the cost of replacing the bike if it is stolen. When
        returning a bike, lock it to a designated Big Red Bikes rack. Lock the bike with the keyhole facing up to help your fellow members.</p>
	
        <h2>Do I have to wear a helmet?</h2>
        <p>For your safety, all users are required to check out a helmet.</p>
	
        <h2>What happens if I break something?</h2>
        <p>We understand that mistakes happen. If you&#39;re honest and report the damages when you check the bike in, we will consider lowering or waiving
        your fine. Fines are equivalent to the cost of replacing the broken part or the entire bike if it&#39;s significantly damaged. Normal wear and tear
        is acceptable, but remember to take care of it like it&#39;s your own bike.</p>
	<p>Please report all damage to the service staff. If a bike returns with unreported or intentional damage, you will be fined and banned from the
        program. Bikes are inspected multiple times per day. Damages can also reported by the next user.</p>
	
        <h2>What happens if I lost the bike or it gets stolen?</h2>
	<p>You are responsible for replacing the bicycle ($700), so remember to lock the bike properly, with the lock in both the wheel and frame. If you
        produce a police report regarding a stolen bike, your liability may be waived, subject to the discretion of Big Red Bikes officers.</p>
	
        <h2>Why does the program close over the winter?</h2>
        <p>Given the maintenance and safety concerns posed by our harsh winters, bikes are put in storage for the winter. Closing (usually November) will be
        announced on this website and our listserv. Opening in the spring (usually March) will be announced in the same manner.</p>
	
        <h2>Can I check out more than one bike at a time?</h2>
	<p>No. Campus groups or departments interested in group rentals should <a href="contact.php" class='page_links'>contact us</a> at least 7 days in
        advance for special arrangements.</p>
	
        <h2>Is Big Red Bikes liable if I get in an accident?</h2>
	<p>No, all users have signed a liability waiver during registration and at every check out instance.</p>
